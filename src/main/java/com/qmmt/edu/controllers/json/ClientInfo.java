package com.qmmt.edu.controllers.json;

public class ClientInfo {
	
	private Long xUid;
	
	private String xClientVersion;
	
	private String xPlatform;
	
	private String origin;
	
	private String xMachineId;
	
	private String xChannelId;
	
	private String xCookie;
	
	private String xPlatformUa;
	
	private String acceptEmojiEncoding;
	
	private String ip;

	private String nickName;
	
	public Long getxUid() {
		return xUid;
	}

	public void setxUid(Long xUid) {
		this.xUid = xUid;
	}

	public String getxClientVersion() {
		return xClientVersion;
	}

	public void setxClientVersion(String xClientVersion) {
		this.xClientVersion = xClientVersion;
	}

	public String getxPlatform() {
		return xPlatform;
	}

	public void setxPlatform(String xPlatform) {
		this.xPlatform = xPlatform;
	}


	public String getxMachineId() {
		return xMachineId;
	}

	public void setxMachineId(String xMachineId) {
		this.xMachineId = xMachineId;
	}

	public String getxChannelId() {
		return xChannelId;
	}

	public void setxChannelId(String xChannelId) {
		this.xChannelId = xChannelId;
	}

	public String getxCookie() {
		return xCookie;
	}

	public void setxCookie(String xCookie) {
		this.xCookie = xCookie;
	}

	public String getxPlatformUa() {
		return xPlatformUa;
	}

	public void setxPlatformUa(String xPlatformUa) {
		this.xPlatformUa = xPlatformUa;
	}

	public String getAcceptEmojiEncoding() {
		return acceptEmojiEncoding;
	}

	public void setAcceptEmojiEncoding(String acceptEmojiEncoding) {
		this.acceptEmojiEncoding = acceptEmojiEncoding;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	
}
